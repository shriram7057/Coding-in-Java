class CharactersEx {
    public static void main(String[] args) {
        char ch1 = 'A', ch2 = 65, ch3 = '\u0041';
        System.out.println(ch1 + " " + ch2 + " " + ch3);

        System.out.println("Newline:\nThis is a new line.");
        System.out.println("Tab:\tThis is tabbed.");
        System.out.println("Backslash: \\");
    }
}
// Demonstrates character data types and escape sequences in Java