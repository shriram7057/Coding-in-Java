class Swapvarbals
{
    public static void main(String args[])
    {
        int a=100,b=200;
        System.out.println("Before swaping");
        System.out.println("a="+a);
        System.out.println("b="+b);
        int temp=a;
        a=b;
        b=temp;
        System.out.println("After swapping");
        System.out.println("a="+a);
        System.out.println("b="+b);

    }
}
