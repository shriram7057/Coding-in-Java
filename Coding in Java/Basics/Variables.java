package Basics;

public class Variables 
{
    public static void main(String[] args) 
    {
        // String variable
        String name = "Swaraa";
        System.out.println(name);

        // Integer variable
        int a = 10;
        System.out.println(a);

        // Float variable
        float b = 10.5f;
        System.out.println(b);

        // Character variable
        char c = 'A';
        System.out.println(c);

        // Boolean variable
        boolean d = true;
        System.out.println(d);
    }
}
