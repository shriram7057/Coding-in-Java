package Basics;

public class IdentifiersExample 
{
    public static void main(String[] args) 
    {
        // Identifiers: myName, age, piValue
        String myName = "Swaraa";   // identifier = myName
        int age = 20;               // identifier = age
        double piValue = 3.14159;   // identifier = piValue

        // Printing identifiers
        System.out.println("Name: " + myName);
        System.out.println("Age: " + age);
        System.out.println("Pi Value: " + piValue);
    }
}
